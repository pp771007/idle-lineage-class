    // 先擷取網址參數:openModal() 的 render 會用 replaceState 覆寫網址,晚讀就拿不到原本的 ?tab= 等
    var _u = { q: _wikiParam('q'), tab: _wikiParam('tab'), cls: _wikiParam('cls') };
    openModal();
